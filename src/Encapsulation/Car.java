package Encapsulation;

public class Car {
	
	private String Brand;
	private int Year;
	
	
	public void setBrand(String Brand) {
		this.Brand=Brand;
	}
	
	public String getBrand() {
		return Brand;
	}
	
	
	public void setYear (int Year) {
		this.Year=Year;
	}
	
	public int getYear() {
		return Year;
	}
	
	
	

}
