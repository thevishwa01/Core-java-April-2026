package com.java.core;

public class DataTypes {
	public static void main(String[] args) {
		int a=125;
		short b=1234;
		long c=987654321l;
		byte d=120;
		float e=25.124f;
		double f=212.12654;
		System.out.println(""+a);
		System.out.println(""+b);
		System.out.println(""+c);
		System.out.println(""+d);
		System.out.println(""+e);
		System.out.println(""+f);
		
	}

}
